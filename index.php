    <h1>Grilled Cheese Sandwich</h1>
    <h2>Ingredients</h2>
    <ul>
        <li>2 slices of bread</li>
        <li>slices of cheddar cheese</li>
        <li>2 tablespoons of butter </li>
</ul>
    <h2>Instructions</h2>
    <ol>
        <li> Butter one side of each slice of bread</li>
        <li>Place a slice of cheese between the slices of bread with the buttered side out</li>
        <li>Heat a non-stick skillet over medium heat</li>
        <li>Place the sandwitch into thr skillet and cook until the bread is golden brown and cheese is melted about 3-4 minutes on each side</li>
        <li>Remove from the skillet and let it cool for a minute</li>
        <li>Cut the sandwitch in half and serve</li>
    </ol> 
